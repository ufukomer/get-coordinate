    private void keyReleased(WindowEvent w, KeyEvent k){
        System.out.println(w.getOppositeWindow());
        if(k.getKeyCode() == KeyEvent.VK_Z){
            PointerInfo a = MouseInfo.getPointerInfo();
                    Point b = a.getLocation();
			int x = (int) b.getX();
			int y = (int) b.getY();
            tableModel.insertRow(jTable1.getRowCount(), new Object[]{x,y});
    }
    }
